# python supports exponentiation by **
# prints: 32
print(2 ** 5)

# prints: 3.0
print(9 ** (1 / 2))

# to determine the quotient of a division, use the floor division which is done using two forward slashes
# prints: 6
print(33 // 5)

# to determine the reminder of a division, use the modulo operator
# prints: 3
print(33 % 5)
